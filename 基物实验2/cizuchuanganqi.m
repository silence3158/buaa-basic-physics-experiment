% --- 1. 数据输入 ---
R = 0.14; % 线圈半径，单位 m

% 坐标网格（归一化位置）
x_norm = [0, 0.05, 0.1, 0.15, 0.2, 0.25, 0.3]; % x/R
y_norm = [0, 0.05, 0.1, 0.15, 0.2, 0.25, 0.3]; % y/R

% 输出电压矩阵 V(x,y) (V)
V = [
    1.037, 1.037, 1.037, 1.036, 1.035, 1.033, 1.029;   % y=0
    1.036, 1.036, 1.036, 1.036, 1.035, 1.033, 1.029;   % y=0.05R
    1.036, 1.036, 1.036, 1.037, 1.037, 1.035, 1.030;   % y=0.1R
    1.035, 1.035, 1.036, 1.036, 1.037, 1.036, 1.034;   % y=0.15R
    1.035, 1.036, 1.037, 1.038, 1.039, 1.040, 1.038;   % y=0.2R
    1.035, 1.035, 1.037, 1.040, 1.043, 1.045, 1.042;   % y=0.25R
    1.032, 1.033, 1.036, 1.039, 1.044, 1.048, 1.049    % y=0.3R
];

% 转换为实际坐标（单位 m）
[X_norm, Y_norm] = meshgrid(x_norm, y_norm);
X = X_norm * R;
Y = Y_norm * R;

% --- 2. 绘制三维曲面图 ---
figure;
surf(X, Y, V);  % 绘制三维曲面
shading interp; % 插值着色，让曲面更光滑
colormap jet;   % 使用jet配色，层次更清晰
colorbar;       % 显示颜色条

% --- 3. 美化图像 ---
xlabel('X 位置 (m)');
ylabel('Y 位置 (m)');
zlabel('输出电压 V (V)');
title('赫姆霍兹线圈空间磁场分布测量');
view(3); % 3D视角，可手动拖动旋转
grid on;
axis tight;